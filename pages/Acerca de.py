st.write("""
Este proyecto está dirigido principalmente al jefe de gestión de riesgos de Ximple, Xavier, quien ha sido nuestro principal contacto y usuario designado del dashboard. Durante este periodo, Xavier nos ha guiado en cada paso para mejorar nuestros modelos de machine learning y los entregables del dashboard, asegurando que nuestra propuesta sea útil y bien estructurada para sus necesidades.
""")

st.header("Storytelling")
